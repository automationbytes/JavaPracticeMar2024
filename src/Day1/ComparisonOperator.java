package Day1;
//comparison operator will giv output in boolean

//logical operator also will giv output in boolean
public class ComparisonOperator {
    public static void main(String[] args) {
        int a =10;
        int b = 20;

        //equal
        System.out.println(a == b);

        //not equals
        System.out.println(a != b);

        //lessthan
        System.out.println(a < b);

        //greater than
        System.out.println(a > b);

        //less than or equals
        System.out.println(a <= b);

        //greater than or equals
        System.out.println(a >= b);

    }
}
